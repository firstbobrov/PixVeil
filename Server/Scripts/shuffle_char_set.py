def shuffle_char_set(seed_key):
    """
    Возвращает перемешанную копию CHAR_SET на основе seed_key.
    
    Args:
        seed_key: числовой ключ для инициализации генератора случайных чисел
        
    Returns:
        list: перемешанная копия CHAR_SET
    """
    char_set_copy = CHAR_SET.copy()
    random.seed(seed_key)
    random.shuffle(char_set_copy)
    return char_set_copy

